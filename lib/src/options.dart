Directory initWorkingDir() {
  final tmpEnvVar = Platform.environment['TMP'];
  if (tmpEnvVar != null) {
    final dir = new Directory(tmpEnvVar);
    if (dir.existsSync()) return dir;
  }
  return Directory.systemTemp;
}

Directory workDir;

